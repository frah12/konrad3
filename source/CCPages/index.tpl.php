<h1>Your pages here?</h1>

<p>For simple static pages, write your <code>html</code>-pages containing only your text and markup that goes between the <code><body></body></code> tags and just drop your <code>HTML</code>-files in the <code>site/pages</code>-directory, and the <code>CCPages</code>-controller will do the rest to load the links in the <em>sidebar</em> and the your content in the <em>primary</em> frame.</p>

<p>If you wish to change the layout of the <em>sidebar</em> you need to edit the <code>source/ccpages/sidebar.tpl.php</code> page.</p>

<p>If you want to edit, or change, the text in this page, just edit the <code>source/ccpages/index.tpl.php</code> page.</p>